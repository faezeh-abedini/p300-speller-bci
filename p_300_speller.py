def resample_epochs(epochs, fs_orig, fs_new):
    # downsampling to 64Hz — P300 peaks around 300ms -->>> 64Hz is ENOUGH!
    # reduces feature dimension ----> before classification
    n_trials, n_samp, n_ch = epochs.shape
    n_new = int(round(n_samp * fs_new / fs_orig))
    out   = np.zeros((n_trials, n_new, n_ch))
    for ch in range(n_ch):
        out[:, :, ch] = signal.resample(epochs[:, :, ch], n_new, axis=1)
    return out


