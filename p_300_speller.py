def reject_artifacts(epochs, labels, threshold=150.0):

    ptp  = (epochs.max(axis=1) - epochs.min(axis=1)).max(axis=1)
    mask = ptp < threshold
    return epochs[mask], labels[mask], int((~mask).sum())


