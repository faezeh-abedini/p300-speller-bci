def find_trigger_onsets(trig):
    onsets, labels = [], []
    prev = 0.0
    for i, val in enumerate(trig):
        if prev == 0.0 and val != 0.0:
            onsets.append(i)
            labels.append(1 if val > 0 else -1)
        prev = val
    return np.array(onsets, dtype=int), np.array(labels, dtype=int)


def extract_epochs(eeg, onsets, labels, fs, t_start=0.0, t_end=0.8):
    # started with -0.1 to 0.7 then switched to 0-0.8
    # For the first version, I used the first 100 ms of each epoch as a simple baseline.
    # A cleaner option would be using a pre-stimulus window!!!

    n0, n1 = int(t_start * fs), int(t_end * fs)
    N = eeg.shape[0]
    epochs, valid_labels = [], []
    for onset, lbl in zip(onsets, labels):
        s, e = onset + n0, onset + n1
        if 0 <= s and e <= N:
            epochs.append(eeg[s:e])
            valid_labels.append(lbl)
    return np.array(epochs), np.array(valid_labels)


