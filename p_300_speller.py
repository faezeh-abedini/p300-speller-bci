def run_subject_classification(epochs, labels, n_comp=4):
    X = np.transpose(epochs, (0, 2, 1))
    y = labels

    skf = StratifiedKFold(n_splits=N_FOLDS, shuffle=True, random_state=42)
    acc_baseline = []
    acc_improved = []

    for train_idx, test_idx in skf.split(X, y):
        X_train, X_test = X[train_idx], X[test_idx]
        y_train, y_test = y[train_idx], y[test_idx]

        xd = XdawnCovariances(nfilter=n_comp, estimator='oas', xdawn_estimator='oas')
        ts = TangentSpace(metric='riemann')

        X_train_cov = xd.fit_transform(X_train, y_train)
        X_test_cov  = xd.transform(X_test)

        X_train_vec = ts.fit_transform(X_train_cov)
        X_test_vec  = ts.transform(X_test_cov)

        # baseline
        clf_base = LinearDiscriminantAnalysis(solver='eigen', shrinkage='auto')
        clf_base.fit(X_train_vec, y_train)
        preds_base = clf_base.predict(X_test_vec)
        acc_baseline.append(balanced_accuracy_score(y_test, preds_base))

        # improve —>>> normalize then PCA then oversample
        scaler = StandardScaler()
        X_train_s = scaler.fit_transform(X_train_vec)
        X_test_s  = scaler.transform(X_test_vec)

        pca = PCA(n_components=0.95)
        X_train_p = pca.fit_transform(X_train_s)
        X_test_p  = pca.transform(X_test_s)  # same transform on test

        X_target    = X_train_p[y_train == 1]
        X_nontarget = X_train_p[y_train == -1]
        y_target    = y_train[y_train == 1]
        y_nontarget = y_train[y_train == -1]

        X_target_up, y_target_up = sk_resample(
            X_target, y_target,
            replace=True,
            n_samples=len(y_nontarget),
            random_state=42
        )

        X_bal = np.vstack((X_nontarget, X_target_up))
        y_bal = np.hstack((y_nontarget, y_target_up))
        perm  = np.random.permutation(len(y_bal))
        X_bal, y_bal = X_bal[perm], y_bal[perm]

        clf_imp = LinearDiscriminantAnalysis(solver='eigen', shrinkage='auto')
        clf_imp.fit(X_bal, y_bal)
        preds_imp = clf_imp.predict(X_test_p)  # X_test_p — same space as X_bal
        acc_improved.append(balanced_accuracy_score(y_test, preds_imp))

    return np.mean(acc_baseline), np.mean(acc_improved)

